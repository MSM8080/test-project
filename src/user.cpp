// -------------------------------------------------------------------
User::User() : _name("\0"), _id(0), _balance(0), _passwd("\0"), _user_exist(0)
{}
// -------------------------------------------------------------------
int User::get_id() const
{
    return _id;
}
// -------------------------------------------------------------------
std::string User::get_name() const
{
    return _name;
}
// -------------------------------------------------------------------
std::string User::get_passwd() const
{
    return _passwd;
}
// -------------------------------------------------------------------
double User::get_balance() const
{
    return _balance;
}
// -------------------------------------------------------------------
void User::set_id(int id)
{
    _id = id;
}
// -------------------------------------------------------------------
void User::set_name(std::string name)
{
    _name = name;
}
// -------------------------------------------------------------------
void User::set_balance(double balance)
{
    _balance = balance;
}
// -------------------------------------------------------------------
void User::set_passwd(std::string passwd)
{
    _passwd = passwd;
}
// -------------------------------------------------------------------
void User::set_exist(bool exist)
{
    _user_exist = exist;
}        
// -------------------------------------------------------------------
void User::increase_balance(double amount)
{
    _balance += amount;
}
// -------------------------------------------------------------------
void User::decrease_balance(double amount)
{
    _balance -= amount;
}
// -------------------------------------------------------------------
bool User::is_exist()
{
    return _user_exist;
}
       


        